class TopicCollector:
    """选题数据收集器（静态数据 + 质量筛选 + 海报获取）"""

    def __init__(self, delay: float = 2.0):
        self.douban = DoubanScraper(delay=delay)
        self.tmdb = TMDBClient(settings.TMDB_API_KEY) if settings.TMDB_API_KEY else None

    async def collect_topics(self, max_count: int = None, topic_type: str = None) -> List[Dict[str, Any]]:
        """
        收集高质量选题数据

        Args:
            max_count: 返回选题数量（None 表示返回所有符合条件的选题）
            topic_type: 指定类型筛选（可选：movie_food, famous_recipe, archaeological）
        """
        logger.info("开始收集选题数据...")

        # 获取已做过的选题
        done_topics = await get_done_topics()
        done_set = set(done_topics)
        logger.info(f"已有 {len(done_topics)} 个已完成选题")

        # 获取已跳过的选题
        skipped_topics = await get_skipped_topics()
        logger.info(f"已有 {len(skipped_topics)} 个已跳过选题")

        # 获取已收藏的选题（收藏的不在发现池显示，只在收藏池）
        favorited_topics = await get_favorites()
        favorited_set = set(favorited_topics)
        logger.info(f"已有 {len(favorited_topics)} 个已收藏选题")

        # 按类型分组收集
        topics_by_type = {
            "movie_food": [],
            "famous_recipe": [],
            "archaeological": []
        }

        for topic in CURATED_TOPICS:
            # 组合标识：作品名·推荐菜品
            topic_key = f"{topic['work_name']}·{topic['recommended_dish']}"

            # 跳过已做过的
            if topic['work_name'] in done_set or topic_key in done_set:
                logger.debug(f"跳过已做过: {topic['work_name']}")
                continue

            # 跳过用户标记为不感兴趣/不适合的
            if topic['id'] in skipped_topics:
                logger.debug(f"跳过已pass: {topic['work_name']}")
                continue

            # 跳过已收藏的（收藏池单独管理）
            if topic['id'] in favorited_set:
                logger.debug(f"跳过已收藏: {topic['work_name']}")
                continue

            # 验证选题质量
            if not self._validate_topic(topic):
                logger.debug(f"质量不达标: {topic['work_name']}")
                continue

            # 计算综合得分
            topic_with_score = {
                **topic,
                "total_score": self._calculate_score(topic),
                "is_done": False,
                "is_favorited": False,
                "collected_at": datetime.now().isoformat(),
                # 添加食材清单
                "ingredients": get_ingredients(topic.get("recommended_dish", ""))
            }

            # 按类型分组
            t_type = topic.get("topic_type", "movie_food")
            if t_type in topics_by_type:
                topics_by_type[t_type].append(topic_with_score)

        # 对每种类型内部按得分排序
        for t_type in topics_by_type:
            topics_by_type[t_type].sort(key=lambda x: x["total_score"], reverse=True)

        # 如果指定了类型，只返回该类型
        if topic_type and topic_type in topics_by_type:
            result = topics_by_type[topic_type]
        else:
            # 返回所有类型的选题，按得分排序
            result = []
            for t_type in topics_by_type:
                result.extend(topics_by_type[t_type])
            result.sort(key=lambda x: x["total_score"], reverse=True)

        # 如果指定了数量限制，截取
        if max_count is not None:
            result = result[:max_count]

        logger.info(f"返回 {len(result)} 个选题 (类型: {topic_type or '全部'})")

        # 并行获取海报（仅针对影视美食类型）
        if self.tmdb:
            movie_food_topics = [t for t in result if t.get("topic_type") == "movie_food"]
            if movie_food_topics:
                logger.info(f"开始并行获取 {len(movie_food_topics)} 个影视美食海报...")

                async def fetch_poster(topic):
                    try:
                        poster_url = await self.tmdb.get_movie_poster(
                            topic["work_name"],
                            topic.get("release_year")
                        )
                        return (topic["id"], poster_url)
                    except Exception as e:
                        logger.debug(f"获取海报失败: {topic['work_name']} - {e}")
                        return (topic["id"], None)

                # 并行获取所有海报
                poster_results = await asyncio.gather(
                    *[fetch_poster(t) for t in movie_food_topics],
                    return_exceptions=True
                )

                # 更新结果
                poster_map = {}
                for r in poster_results:
                    if isinstance(r, tuple):
                        poster_map[r[0]] = r[1]

                for topic in result:
                    if topic["id"] in poster_map:
                        topic["poster_url"] = poster_map[topic["id"]]

                logger.info(f"海报获取完成，成功 {len([p for p in poster_map.values() if p])} 个")

        logger.info(f"返回 {len(result)} 个高质量选题")

        return result

    def _validate_topic(self, topic: Dict[str, Any]) -> bool:
        """验证选题质量"""
        # 1. 推荐菜品必须存在且具体
        dish = topic.get("recommended_dish", "")
        if not dish or len(dish) < 2:
            return False

        # 排除模糊词
        vague_terms = ["料理", "大餐", "家宴", "小吃", "美食", "菜系"]
        if any(term in dish for term in vague_terms):
            # 如果只有模糊词，不通过
            cleaned = dish
            for term in vague_terms:
                cleaned = cleaned.replace(term, "")
            if len(cleaned.strip()) < 2:
                return False

        # 2. 至少2个故事切入点
        angles = topic.get("story_angles", [])
        if len(angles) < 2:
            return False

        # 3. 三有评分至少满足2项
        score_count = sum([
            topic.get("is_interesting", False),
            topic.get("is_discussable", False),
            topic.get("has_momentum", False)
        ])
        if score_count < 2:
            return False

        # 4. 烹饪难度不能超出能力
        if topic.get("cooking_difficulty") == "超出能力":
            return False

        # 5. 视觉复杂度不能太低
        if topic.get("visual_complexity") == "低":
            return False

        return True

    def _calculate_score(self, topic: Dict[str, Any]) -> float:
        """计算选题综合得分（根据类型采用不同评分逻辑）"""
        score = 0.0
        topic_type = topic.get("topic_type", "movie_food")

        # 基础分（让不同类型有公平起点）
        base_scores = {
            "movie_food": 0,
            "famous_recipe": 40,  # 补偿没有豆瓣分
            "archaeological": 45  # 考古美食稍高，因为有稀缺性
        }
        score += base_scores.get(topic_type, 0)

        # 类型特定评分
        if topic_type == "movie_food":
            # 影视美食：豆瓣分权重
            score += topic.get("douban_score", 0) * 5
        elif topic_type == "famous_recipe":
            # 名店配方：米其林星级、配方来源类型
            stars = topic.get("michelin_stars", 0) or 0
            score += stars * 15
            if topic.get("recipe_source_type") == "官方书籍":
                score += 10
            elif topic.get("recipe_source_type") == "主厨采访":
                score += 8
        elif topic_type == "archaeological":
            # 考古美食：年代越久远分数越高，有史料来源加分
            year = topic.get("year_origin", 2000)
            if year:
                age = 2025 - year
                score += min(age / 10, 20)  # 最多加20分
            if topic.get("historical_source_url"):
                score += 15  # 有史料链接大加分

        # 故事切入点分数（通用）
        for angle in topic.get("story_angles", []):
            score += angle.get("potential_score", 0)

        # 三有加分（通用）
        if topic.get("is_interesting"):
            score += 10
        if topic.get("is_discussable"):
            score += 8
        if topic.get("has_momentum"):
            score += 15  # 热点加权更高

        # 烹饪难度调整（通用）
        difficulty = topic.get("cooking_difficulty", "中等")
        if difficulty == "简单":
            score += 5
        elif difficulty == "中等":
            score += 2
        elif difficulty == "困难":
            score -= 5

        # 视觉复杂度加分（通用）
        visual = topic.get("visual_complexity", "中")
        if visual == "高":
            score += 5
        elif visual == "低":
            score -= 10

        return score

    async def collect_raw_topics(self, max_movies: int = 20, try_online: bool = False) -> List[Dict[str, Any]]:
        """
        兼容旧接口：收集原始选题数据（简化格式）
        支持三种选题类型
        """
        topics = await self.collect_topics(max_count=max_movies)

        source_labels = {
            "movie_food": "美食电影精选",
            "famous_recipe": "名店配方精选",
            "archaeological": "考古美食精选"
        }

        # 转换为简化格式
        raw_topics = []
        for t in topics:
            topic_type = t.get("topic_type", "movie_food")
            raw_topics.append({
                "title": t["work_name"],
                "score": t.get("douban_score") or t.get("michelin_stars", 0) * 3 or 8.0,
                "year": t.get("release_year") or t.get("year_origin") or 2024,
                "url": t.get("douban_url") or t.get("recipe_source_url") or t.get("historical_source_url") or "",
                "food_hint": t.get("recommended_dish", ""),
                "source": source_labels.get(topic_type, "美食精选"),
                "collected_at": t["collected_at"],
                "topic_type": topic_type
            })

        return raw_topics

